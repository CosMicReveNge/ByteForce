  const StatisticsScreen({super.key});

  @override
  Widget build(BuildContext context) {
    return Scaffold(
      appBar: AppBar(title: const Text('Statistics')),
      body: SingleChildScrollView(
        padding: const EdgeInsets.all(16.0),
        child: Column(
          crossAxisAlignment: CrossAxisAlignment.start,
          children: [
            // Summary
            Card(
              child: Padding(
                padding: const EdgeInsets.all(16.0),
                child: Column(
                  crossAxisAlignment: CrossAxisAlignment.start,
                  children: [
                    const Text(
                      'Summary',
                      style: TextStyle(
                        fontSize: 18,
                        fontWeight: FontWeight.bold,
                      ),
                    ),
                    const SizedBox(height: 16),
                    _buildSummaryItem(context, 'Total manga', '42'),
                    _buildSummaryItem(context, 'Total chapters read', '1,337'),
                    _buildSummaryItem(context, 'Reading time', '127 hours'),
                    _buildSummaryItem(
                      context,
                      'Average daily reading',
                      '1.5 hours',
                    ),
                  ],
                ),
              ),
            ),

            const SizedBox(height: 16),

            // Reading activity
            Card(
              child: Padding(
                padding: const EdgeInsets.all(16.0),
